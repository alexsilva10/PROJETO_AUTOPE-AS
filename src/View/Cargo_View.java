     public void atualizarTabela() {
        cargo = new Cargo_Model();
        try {
            cargos = cargo_Dao.todosCargos();
            String dados[][] = new String[cargos.size()][2];
            int i = 0;
            for (Cargo_Model u : cargos) {
                dados[i][0] = String.valueOf(u.getID());
                dados[i][1] = u.getNomecargo();
                 
                i++;
            }
            String tituloColuna[] = {"ID", "Cargo"};
            modelo.setDataVector(dados, tituloColuna);
            Tabela_Cargo.setModel(modelo);
            Tabela_Cargo.updateUI();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }    
     }

